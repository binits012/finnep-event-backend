/**
 * Validate ISO 3166-1 alpha-2 country codes without adding dependencies.
 * Uses Intl.supportedValuesOf('region') when available (Node 18+), else a safe fallback.
 */

function buildAlpha2SetFromIntl() {
  try {
    if (typeof Intl !== 'undefined' && typeof Intl.supportedValuesOf === 'function') {
      const codes = Intl.supportedValuesOf('region').filter((r) => typeof r === 'string' && /^[A-Z]{2}$/.test(r));
      return new Set(codes);
    }
  } catch {
    // ignore
  }
  return null;
}

