const ISO_ALPHA2 = buildAlpha2SetFromIntl() || FALLBACK_ALPHA2;

export function isValidIso3166Alpha2(code) {
  return typeof code === 'string' && ISO_ALPHA2.has(code);
}

/**
 * @param {unknown} raw
 * @returns {string|null} uppercase alpha-2 or null if invalid
 */
export function normalizeIso3166Alpha2(raw) {
  if (raw == null || typeof raw !== 'string') return null;
  const u = raw.trim().toUpperCase();
  if (!/^[A-Z]{2}$/.test(u) || !ISO_ALPHA2.has(u)) return null;
  return u;
}
